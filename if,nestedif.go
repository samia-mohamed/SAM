package main

import "fmt"

func main() {

	var a int = 500

	if a == 100 {

		fmt.Printf("Value of a is 100\n")
	} else if a == 200 {

		fmt.Printf("Value of a is 200\n")
	} else if a == 300 {

		fmt.Printf("Value of a is 300\n")
	} else {

		fmt.Printf("None of the values is matching\n")
	}
}
